# Importation du module
from math import pi, sin, sqrt

# Manipulation des constantes (pi, ...) et des fonctions (sqrt, sin, ...)
nombre = 121
angle = pi / 6 # soit 30°

print(f"racine carrée de {nombre} = {sqrt(nombre)}")
print(f"sinus de {angle} radians = {sin(angle)}")